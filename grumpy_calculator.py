import time
import random
import sys

def grumpy_calculator():
    print("در حال روشن کردن موتورهای پردازش سنگین...")
    time.sleep(2)
    print("آه... دوباره یه کاربر جدید؟ باشه، بگو چی می‌خوای حساب کنی، ولی لطفاً وقتمو نگیر.")
    time.sleep(1)

    print("\n--- 🛑 سیستم محاسبات فوق پیشرفته (نسخه بی‌اعصاب) 🛑 ---")
    
    try:
        num1 = float(input("عدد اول رو تایپ کن (امیدوارم حداقل اینو درست بزنی): "))
        op = input("عملگر (+, -, *, /) رو وارد کن (فقط همین چهارتا رو بلدم، توقع بی‌جا نداشته باش): ")
        num2 = float(input("عدد دوم رو بگو (سریع باش، کار دارم): "))
    except ValueError:
        print("\nخطا! فرق عدد و حروف رو نمی‌دونی؟ من با این سطح از هوش کار نمی‌کنم. خداحافظ!")
        sys.exit()

    print("\nداره پردازش میشه... اوه خدای من... این دیگه چه معادله‌ایه...")
    # اینجا الکی کاربر رو معطل می‌کنیم
    time.sleep(3)

    # گیر دادن به سوالات خیلی آسون
    if num1 == 1 and num2 == 1 and op == '+':
        print("\nواقعاً؟! یک به علاوه یک؟! واسه این منو روشن کردی؟ برو خجالت بکش. جواب میشه ۲. حالا هم ولم کن.")
        return

    # محاسبه واقعی
    if op == '+': result = num1 + num2
    elif op == '-': result = num1 - num2
    elif op == '*': result = num1 * num2
    elif op == '/':
        if num2 == 0:
            print("\nتقسیم بر صفر؟! می‌خوای کائنات رو نابود کنی؟ من تو این جنایت شریک نمیشم!")
            return
        result = num1 / num2
    else:
        print("\nعملگرت اشتباهه. تقصیر سیستم نیست، تو بلد نیستی تایپ کنی.")
        return

    # اضافه کردن خطای عمدی و رو اعصاب برای طبیعی جلوه دادن
    error_margin = random.choice([0.0001, -0.0002, 0.0000005, 0])
    final_result = result + error_margin

    print(f"\n✅ جواب نهایی (احتمالاً): {final_result}")
    
    if error_margin != 0:
        print("⚠️ نکته مهم: اگه ماشین حساب گوشیت یه جواب دیگه داد، بدون اون ضعیفه و اشتباه می‌کنه. من نوسانات کوانتومی فضا-زمان رو هم تو این جواب حساب کردم!")

grumpy_calculator()
